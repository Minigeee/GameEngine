#include <Scene/GameObject.h>

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

GameObject::GameObject(Scene* scene, GameObjectID id) :
	mScene		(scene),
	mID			(id)
{

}

///////////////////////////////////////////////////////////////////////////////

GameObjectID GameObject::GetID() const
{
	return mID;
}

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

GameObjectID::GameObjectID() :
	mTypeID			(0)
{

}

GameObjectID::GameObjectID(::Handle handle, Uint16 typeID) :
	mHandle		(handle),
	mTypeID		(typeID)
{

}

///////////////////////////////////////////////////////////////////////////////

GameObjectID::operator Uint32() const
{
	return (((Uint32)mHandle) << 16) | (Uint32)mTypeID;
}

Handle GameObjectID::Handle() const
{
	return mHandle;
}

Uint16 GameObjectID::TypeID() const
{
	return mTypeID;
}

bool GameObjectID::Exists() const
{
	return (bool)mTypeID;
}

