#include <Math/BoundingBox.h>

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

BoundingBox::BoundingBox() :
	mMin		(0.0f),
	mMax		(0.0f)
{

}

BoundingBox::BoundingBox(const Vector3f& min, const Vector3f& max) :
	mMin		(min),
	mMax		(max)
{

}

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

Vector3f BoundingBox::GetPosition() const
{
	return (mMin + mMax) * 0.5f;
}

