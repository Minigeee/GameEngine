#include <Math/Plane.h>

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

Plane::Plane(const Vector3f& n, float d)
{
	float mag = Length(n);
	this->n = n / mag;
	this->d = d / mag;
}

Plane::Plane(float a, float b, float c, float d) :
	n(a, b, c),
	d(d)
{
	float mag = Length(n);
	this->n /= mag;
	this->d /= mag;
}

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

float Plane::Dist(const Vector3f& p) const
{
	return Dot(n, p) + d;
}

