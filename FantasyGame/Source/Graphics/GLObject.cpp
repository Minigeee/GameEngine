#include <Graphics/GLObject.h>

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

GLObject::GLObject() :
	mID			(0)
{

}

GLObject::~GLObject()
{

}

///////////////////////////////////////////////////////////////////////////////

GLObject::GLObject(GLObject&& other) :
	mID			(0)
{
	mID = other.mID;
	other.mID = 0;
}

GLObject& GLObject::operator=(GLObject&& other)
{
	if (this != &other)
	{
		mID = other.mID;
		other.mID = 0;
	}

	return *this;
}

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

Uint32 GLObject::GetID() const
{
	return mID;
}

