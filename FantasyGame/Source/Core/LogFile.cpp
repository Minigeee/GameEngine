#include <Core/LogFile.h>

///////////////////////////////////////////////////////////////////////////////

LogFile LogFile::gLogFile("game.log");

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

LogFile::LogFile(const char* fname)
{
	mFile.open(fname);
	*this << "=============== Log Start ===============\n\n";
}

///////////////////////////////////////////////////////////////////////////////

LogFile::~LogFile()
{
	*this << "\n\n=============== Log Stop ===============\n";
	mFile.close();
}

